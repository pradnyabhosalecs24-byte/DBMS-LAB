CREATE DATABASE dnya;
USE dnya;
create table Dept(DEPTNO INT PRIMARY KEY,DNAME VARCHAR(30),DLOC VARCHAR(30));
create table EMPLOYEE (EMPNO INT PRIMARY KEY,ENAME VARCHAR(30),MGR_NO INT,HIREDATE DATE,SAL DECIMAL(10,2),
DEPTNO INT,FOREIGN KEY (DEPTNO) REFERENCES DEPT(DEPTNO));
create table PROJECT (PNO INT PRIMARY KEY,PLOC VARCHAR(30),PNAME VARCHAR(40));
CREATE TABLE ASSIGNED_TO (EMPNO INT,PNO INT,JOB_ROLE VARCHAR(30),PRIMARY KEY (EMPNO, PNO),
FOREIGN KEY (EMPNO) REFERENCES EMPLOYEE(EMPNO),FOREIGN KEY (PNO) REFERENCES PROJECT(PNO));
CREATE TABLE INCENTIVES (EMPNO INT,INCENTIVE_DATE DATE,INCENTIVE_AMOUNT DECIMAL(10,2),FOREIGN KEY (EMPNO) REFERENCES EMPLOYEE(EMPNO));
